// Export comparison analysis as Markdown or prepare for PDF generation

export interface AnalysisData {
  summary: string;
  keyChanges: string[];
  designChanges: string[];
  contentChanges: string[];
  businessInsights: string[];
}

export interface ExportData {
  url: string;
  earlierDate: string;
  laterDate: string;
  analysis: AnalysisData;
  generatedAt: string;
}

// Generate Markdown report
export function generateMarkdownReport(data: ExportData): string {
  const { url, earlierDate, laterDate, analysis, generatedAt } = data;
  
  return `# TimeBack Analysis Report

**Website:** ${url}
**Comparison Period:** ${earlierDate} ‚Üí ${laterDate}
**Generated:** ${generatedAt}

---

## Summary

${analysis.summary}

---

## Key Changes

${analysis.keyChanges.map((change, i) => `${i + 1}. ${change}`).join('\n')}

---

## Design Evolution

${analysis.designChanges.map(change => `- ${change}`).join('\n')}

---

## Content Shifts

${analysis.contentChanges.map(change => `- ${change}`).join('\n')}

---

## Business Insights

${analysis.businessInsights.map(insight => `> ${insight}`).join('\n\n')}

---

## Archived Snapshots

- **Earlier:** [View on Wayback Machine](https://web.archive.org/web/${earlierDate.replace(/-/g, '')}/${url})
- **Later:** [View on Wayback Machine](https://web.archive.org/web/${laterDate.replace(/-/g, '')}/${url})

---

*Report generated by [TimeBack](https://timeback.vercel.app) - Visual Website Time Machine*
`;
}

