// Download helper - creates a downloadable file
export function downloadFile(content: string, filename: string, mimeType: string): void {
  const blob = new Blob([content], { type: mimeType });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = filename;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  URL.revokeObjectURL(url);
}

// Download as Markdown
export function downloadMarkdown(data: ExportData): void {
  const markdown = generateMarkdownReport(data);
  const filename = `timeback-${data.url.replace(/[^a-zA-Z0-9]/g, '-')}-${data.laterDate}.md`;
  downloadFile(markdown, filename, 'text/markdown');
}

// Download as HTML (can be printed to PDF from browser)
export function downloadHTML(data: ExportData): void {
  const html = generateHTMLReport(data);
  const filename = `timeback-${data.url.replace(/[^a-zA-Z0-9]/g, '-')}-${data.laterDate}.html`;
  downloadFile(html, filename, 'text/html');
}

// Open HTML in new tab for printing
export function openForPrint(data: ExportData): void {
  const html = generateHTMLReport(data);
  const newWindow = window.open();
  if (newWindow) {
    newWindow.document.write(html);
    newWindow.document.close();
    // Wait for content to load then trigger print
    setTimeout(() => {
      newWindow.print();
    }, 500);
  }
}
